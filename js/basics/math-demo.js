/**
 * In this section, we will learn about math functions in JavaScript.
 * Math functions are used to perform mathematical operations on numbers.
 * JavaScript provides several built-in math functions, including:
 * 1. Math.abs(x): Returns the absolute value of x.
 * 2. Math.ceil(x): Returns the smallest integer greater than or equal to x.
 * 3. Math.floor(x): Returns the largest integer less than or equal to x.
 * 4. Math.round(x): Returns the nearest integer to x.
 */

let x = 5;
let y = 2.5;


console.log(`truncate of ${y}`, Math.trunc(y));
console.log(`pow of ${x}` , Math.pow(x, 2));
console.log(`sqrt of ${x}`, Math.sqrt(x));
console.log(`abs of ${x}`, Math.abs(x));
console.log(`ceil of ${x}`, Math.ceil(x));
console.log(`floor of ${x}`, Math.floor(x));
console.log(`round of ${x}`, Math.round(x));