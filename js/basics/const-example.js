/**
 * In this section, we will learn about the const keyword in JavaScript.
 * The const keyword is used to declare a constant variable, which means that its value cannot be changed once it is assigned.
 */

// Calculate the area & circumference of a circle

let radium = 5;
const PI = 3.14159;

let area = PI * radium * radium;
let circumference = 2 * PI * radium;


console.log(`Area of circle is ${area}`);
console.log(`Circumference of circle is ${circumference}`);


