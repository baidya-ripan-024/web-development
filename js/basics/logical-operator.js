/**
 * In this section, we will explore the logical operators in JavaScript.
 * Logical operators are used to combine two or more expressions and return a boolean value (true or false).
 * The three main logical operators in JavaScript are:
 * - AND (&&)
 * - OR (||)
 * - NOT (!)
 */

let a = true;
let b = false;


// AND operator (&&)
console.log(a && b); // false
console.log(a && true); // true

// OR operator (||)
console.log(a || b); // true
console.log(b || false); // false

// NOT operator (!)
console.log(!a); // false